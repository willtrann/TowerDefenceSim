package WizardTD;

/**
 * The type Coordinate.
 */
public class Coordinate {
    /**
     * The X.
     */
    float x, /**
     * The Y.
     */
    y;

    /**
     * Instantiates a new Coordinate.
     *
     * @param x the x
     * @param y the y
     */
    Coordinate(float x, float y) {
        this.x = x; // x and y coordinate of coordinate
        this.y = y;
    }

}